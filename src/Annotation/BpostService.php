<?php

namespace Drupal\commerce_bpost\Annotation;

use Drupal\Component\Annotation\Plugin;

/**
 * Defines bpost_service annotation object.
 *
 * @Annotation
 */
class BpostService extends Plugin {

  /**
   * The plugin ID.
   *
   * @var string
   */
  public $id;

  /**
   * The human-readable name of the plugin.
   *
   * @var \Drupal\Core\Annotation\Translation
   *
   * @ingroup plugin_translatable
   */
  public $title;

  /**
   * The description of the plugin.
   *
   * @var \Drupal\Core\Annotation\Translation
   *
   * @ingroup plugin_translatable
   */
  public $description;

}
